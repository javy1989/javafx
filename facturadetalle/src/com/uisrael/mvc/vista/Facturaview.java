    private String guardar() throws Exception {

        validar();
        Usuario usuario = null;

        if (accion == true) {
            usuario = new Usuario();
        } else {
            usuario = factura.getUsuario();
        }

        usuario.setDocumento(txtDocumento.getText().trim());
        usuario.setNombre(txtNombre.getText().trim());
        usuario.setApellido(txtApellido.getText().trim());
        usuario.setDireccion(txtDireccion.getText() == null ? "" : txtDireccion.getText().trim());
        usuario.setTelefono(txtTelefono.getText() == null ? "" : txtTelefono.getText().trim());
        if (usuario.getId_usuario() == 0) {
            controllerUsuario.guardar(usuario);
        } else {
            controllerUsuario.editar(usuario);
        }

        usuario = null;
        usuario = controllerUsuario.getUsuarioFindByDocumento(txtDocumento.getText().trim());
        UUID codigo = UUID.randomUUID();
        if (accion == true) {
            factura.setCodigo(codigo.toString());
        }

        factura.setFecha(new Date());
        factura.setTotal(total);
        factura.setUsuario(usuario);
        if (factura.getId_factura() == 0) {
            controllerFactura.guardar(factura);
        } else {
            controllerFactura.editar(factura);
        }

        if (factura.getId_factura() == 0) {
            factura = null;
            factura = controllerFactura.getFacturaByCodigo(codigo.toString());
        }

        for (Detalle d : detalles) {
            d.setFactura(factura);
            if (d.getId_detalle() == 0) {
                controllerDetalle.guardar(d);
            } else {
                controllerDetalle.editar(d);
            }
        }

        return null;
    }

    private String validar() {
        if (txtDocumento.getText() == null || txtDocumento.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Documento necesario");
            return null;
        }

        if (txtNombre.getText() == null || txtNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nombre necesario");
            return null;
        }
        if (txtApellido.getText() == null || txtApellido.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Apellido necesario");
            return null;
        }

        if (detalles.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar un producto por lo menos");
            return null;
        }
        return null;
    }
