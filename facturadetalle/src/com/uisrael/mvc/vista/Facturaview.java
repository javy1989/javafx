    public void crearDataModel() {
        if (modeloDetalle == null) {
            modeloDetalle = new DefaultTableModel();
        } else {
            modeloDetalle.getDataVector().removeAllElements();
            modeloDetalle.fireTableDataChanged();
        }

        ArrayList<Object> columnas = new ArrayList<>();
        columnas.add("Producto");
        columnas.add("Valor");
        columnas.add("Cantidad");
        for (Object columna : columnas) {
            modeloDetalle.addColumn(columna);
        }

        dt_detalle.setModel(modeloDetalle);
        detalles = new LinkedList<>();
        if (accion == false) {
            controllerDetalle = DetalleController.getInstance();
            detalles = controllerDetalle.getDetalle(factura.getId_factura());
            txtDocumento.setText(factura.getUsuario().getDocumento());
            txtNombre.setText(factura.getUsuario().getNombre());
            txtApellido.setText(factura.getUsuario().getApellido());
            txtTelefono.setText(factura.getUsuario().getTelefono());
            txtDireccion.setText(factura.getUsuario().getDireccion());
            txtTotal.setText(factura.getTotal().toString());
            initData();
        }

    }

    private void initData() {
        int contFilas = detalles.size();
        Object[] fila = new Object[3];
        modeloDetalle.getDataVector().removeAllElements();
        modeloDetalle.fireTableDataChanged();
        for (Detalle det : detalles) {
            fila[0] = det.getProducto();
            fila[1] = det.getValor();
            fila[2] = det.getCantidad();
            modeloDetalle.addRow(fila);
        }
        modeloDetalle.fireTableDataChanged();
        detalleSelect = null;
    }

