    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int telefono = princial.selectEstudiante.getId();
        Estudiante nuevo = new Estudiante();
        nuevo.setId(telefono);
        nuevo.setNombre(jtxtNombre.getText());
        nuevo.setApellido(jtxtApellido.getText());
        nuevo.setDireccion(jtxtDireccion.getText());
        nuevo.setTelefono(jtxtFono.getText());
        estudianteController.actualizarUsuario(nuevo);
        JOptionPane.showMessageDialog(null,"Estudiante actualizado correctamente");
        jtxtNombre.setText("");
        jtxtApellido.setText("");
        jtxtDireccion.setText("");
        jtxtFono.setText("");
        princial.selectEstudiante.setId(0);
        princial.selectEstudiante.setNombre("");
        princial.selectEstudiante.setApellido("");
        princial.selectEstudiante.setDireccion("");
        princial.selectEstudiante.setTelefono("");
        princial.listarDatos();
        this.dispose();
       
        
    }//GEN-LAST:event_jButton1ActionPerformed

